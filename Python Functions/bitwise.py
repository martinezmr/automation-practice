#bitwise operators
# & compares like 1 bits (binary) between both integers
print(15 & 22)

# | as long as a 1 bit is seen per integer, it will output it
print(15 | 22)

# ^ only returns data if only 1 bit is a 1, if both are 1 it returns a 0
print(15 ^ 22)

# bit shifting - shift all bits to the right by one
print(22 >> 1)

# bit shifting - shift all bits to the left by one
print(22 << 1)